    private boolean existeDigito(String nome) {
        for (int i = 0; i < nome.length(); i++) {
            if (nome.charAt(i) >= '0' && nome.charAt(i) <= '9') {
                return true;
            }
        }
        return false;
    }

    // Metodo que conta quantos digitos existem em uma variavel String
    private int contaDigitos(String numero) {
        int contador = 0;
        for (int i = 0; i < numero.length(); i++) {
            if (numero.charAt(i) >= '0' && numero.charAt(i) <= '9') {
                contador++;
            }
        }
        return contador;
    }

    private boolean testaSenha(String senha) {
        int contador = 0;
        for (int i = 0; i < senha.length(); i++) {
            if (senha.charAt(i) >= '0' && senha.charAt(i) <= '9') {
                contador++;
            }
        }
        // pelo menos um numero
        if (contador >= 1) {
            contador = 0;
            for (int i = 0; i < senha.length(); i++) {
                if (senha.charAt(i) >= 'A' && senha.charAt(i) <= 'Z' || senha.charAt(i) >= 'a' && senha.charAt(i) <= 'z') {
                    contador++;
                }
            }
            // pelo menos uma letra
            if (contador >= 1) {
                return true;
            }
        }
        return false;
    }
